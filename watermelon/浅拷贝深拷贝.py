#浅拷贝
#只复制最外层对象，内部的可变对象仍然共享引用
import copy
a = [ 1,2,[3,4]]
b = copy.copy(a)

b.append(5)
b[2].append(6)

print(a)
print(b)


# a (变量) → 内存地址 0x100            b (变量) → 内存地址 0x300（新列表！）
#             ┌─────────────────┐                ┌─────────────────┐
#             │ 索引0: 1        │                │ 索引0: 1        │  ← 复制了值
#             │ 索引1: 2        │                │ 索引1: 2        │  ← 复制了值
#             │ 索引2: ●──────┐ │                │ 索引2: ●──────┐ │  ← 复制了引用（指针）
#             └─────────────────┘                └─────────────────┘ 
#                               │                                  │
#                               ▼                                  │
#                         内存地址 0x200                           │
#                         ┌──────────────┐                        │
#                         │ 索引0: 3     │ ◄──────────────────────┘
#                         │ 索引1: 4     │      ↑ 注意：两个列表的索引2
#                         └──────────────┘      │ 指向同一个地方！

print("------------------------------------------------------------")

#深拷贝
#递归复制所有层级，完全独立，互不影响。
c = [1,2,[3,4]]
d = copy.deepcopy(c)

d.append(5)

d[2].append(9)
print(c)
print(d)


# c ──→ 内存地址 0x100          d ──→ 内存地址 0x300（新外层列表）
#        ┌─────────────────┐       ┌─────────────────┐
#        │ 索引0: 1        │       │ 索引0: 1        │  ← 复制值
#        │ 索引1: 2        │       │ 索引1: 2        │  ← 复制值
#        │ 索引2: ●──────┐ │       │ 索引2: ●──────┐ │  ← 新指针！
#        └─────────────────┘ │       └─────────────────┘ │
#                          │                         │
#                          ▼                         ▼
#                    内存地址 0x200             内存地址 0x400（新内层列表）
#                    ┌──────────────┐          ┌──────────────┐
#                    │ 索引0: 3     │          │ 索引0: 3     │  ← 复制值
#                    │ 索引1: 4     │          │ 索引1: 4     │  ← 复制值
#                    └──────────────┘          └──────────────┘